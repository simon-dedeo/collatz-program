def scc(nodes, adj):
    idx = {}; low = {}; on = {}; stk = []; out = []; c = [0]
    sys.setrecursionlimit(1 << 20)
    def strong(v):
        idx[v] = low[v] = c[0]; c[0] += 1; stk.append(v); on[v] = True
        for w in adj.get(v, ()):
            if w not in idx:
                strong(w); low[v] = min(low[v], low[w])
            elif on.get(w):
                low[v] = min(low[v], idx[w])
        if low[v] == idx[v]:
            comp = []
            while True:
                w = stk.pop(); on[w] = False; comp.append(w)
                if w == v:
                    break
            out.append(comp)
    for v in nodes:
        if v not in idx:
            strong(v)
    return out


def perm_apply(g, v):
    """g=(mul,add): (M v)_j = v_[(add+mul*j)%3]."""
    mul, add = g
    return tuple(v[(add + mul * j) % 3] for j in range(3))


def fixes_nonconstant(g):
    """Does affine perm g fix a nonconstant vector?  True unless g is a 3-cycle
    (translation by +-1).  Also test the specific co-spine ray (2,-1,-1)."""
    mul, add = g
    is_3cycle = (mul == 1 and add != 0)
    cospine = perm_apply(g, (2, -1, -1)) == (2, -1, -1)
    return (not is_3cycle), cospine


def analyse(J, Lw):
    S = am.states(J)
    E = set(am.exceptional_set(J))
    C = [q for q in S if q not in E]
    Cset = set(C)
    edges = am.edges(J)
    # zero-charge induced edges (both endpoints in C); tag kind
    zc_edges = [(s, d, k) for (s, d, k) in edges if s in Cset and d in Cset]
    adjC = {q: set() for q in C}
    for (s, d, k) in zc_edges:
        adjC[s].add(d)
    comps = scc(C, adjC)
    nontriv = [comp for comp in comps if len(comp) > 1]
    selfloops = [(s, k) for (s, d, k) in zc_edges if s == d]
    # holonomy: aligned-face perm per kind (add=0)
    perm = {'T': (1, 0), 'B2': (1, 0), 'B8': (2, 0)}
    # semigroup generated by perms on zero-charge edges (aligned face)
    gens = set(perm[k] for (_, _, k) in zc_edges)

    def compose(g2, g1):
        m1, a1 = g1; m2, a2 = g2
        return ((m2 * m1) % 3, (m2 * a1 + a2) % 3)
    semi = set(gens)
    changed = True
    while changed:
        changed = False
        for a in list(semi):
            for b in gens:
                c = compose(a, b)
                if c not in semi:
                    semi.add(c); changed = True
    # persistence: exists zero-charge cycle whose holonomy fixes a nonconstant vec.
    # simplest witnesses: (i) B2 self-loop (id -> fixes everything);
    # (ii) any cycle; net holonomy on aligned face is in {id,swap} <= stabilizer.
    persists = bool(selfloops or nontriv)
    # holonomy elements realizable on cycles (aligned face) are in semi; each is
    # id or swap (mul in {1,2}, add 0) -> all fix (2,-1,-1).
    stab_hits = [g for g in semi if fixes_nonconstant(g)[1]]
    all_fix_nonconst = all(fixes_nonconstant(g)[0] for g in semi)
    return {
        'J': J, 'Lw': Lw, 'nQ': len(S), 'nE': len(E), 'E': sorted(E),
        'nC': len(C), 'n_selfloops': len(selfloops),
        'selfloop_kinds': sorted(set(k for (_, k) in selfloops)),
        'n_nontriv_scc': len(nontriv),
        'largest_scc': max((len(c) for c in comps), default=0),
        'semigroup': sorted(semi), 'semigroup_size': len(semi),
        'semigroup_in_cospine_stabilizer': (set(semi) <= {(1, 0), (2, 0)}),
        'all_holonomy_fix_nonconstant': all_fix_nonconst,
        'zero_charge_cycle_exists': persists,
        'obstruction_persists': persists,
    }


