#!/usr/bin/env python3
"""Bounded exact audit of the annealed pair-carry Green kernel.

At the critical annealed endpoint, unequal induced blocks with the same first
free ternary digit define affine tail alignments

    tau_(e,f) = H_f^(-1) o H_e.

After writing ``x = 2 + 3*u``, all such alignments are generated by the
two-state path system

    even --(weight 1/2,  T(u)=2*u+1)--> odd,
    odd  --(weight 1/32, V(u)=32*u+24)--> even.

The starting squared block masses are 1/15 on the even state and 4/15 on
the odd state.  Sum all nonempty forward paths, add their inverses, and
multiply by 21/2.  This script checks that description in exact rational
arithmetic and compares it with the independently implemented alignment
kernel in ``verify_annealed_critical_coding.py``.

The audit verifies three nontrivial finite quotients (3, 9, and 27 parent
states), finite-level projectivity and martingale identities, the exact first
detail-shell correlation, and closed-form geometric weights.  It does *not*
prove the affine local-limit statement, convergence of the collision
multiplier, or any asymptotic decay estimate.
"""

from __future__ import annotations

from fractions import Fraction
from typing import TypeAlias

from verify_annealed_critical_coding import (
    alignment_kernel,
    image_distribution,
    next_marginal,
)


Affine: TypeAlias = tuple[Fraction, Fraction]
Matrix: TypeAlias = tuple[tuple[Fraction, ...], ...]

EVEN = 0
ODD = 1
Q = Fraction(1, 64)
NORMALIZATION = Fraction(21, 2)
START_WEIGHT = {
    EVEN: Fraction(1, 15),
    ODD: Fraction(4, 15),
}

# The x-coordinate maps preserve Y = 2 + 3 Z_3.
T_X: Affine = (Fraction(2), Fraction(1))
V_X: Affine = (Fraction(32), Fraction(10))

# The corresponding maps after x = 2 + 3u.
T_U: Affine = (Fraction(2), Fraction(1))
V_U: Affine = (Fraction(32), Fraction(24))


def compose(outer: Affine, inner: Affine) -> Affine:
    """Return ``outer o inner`` for affine maps over the rationals."""

    outer_slope, outer_offset = outer
    inner_slope, inner_offset = inner
    return (
        outer_slope * inner_slope,
        outer_slope * inner_offset + outer_offset,
    )


def inverse(function: Affine) -> Affine:
    slope, offset = function
    return Fraction(1, slope), -offset / slope


def compose_mod(
    outer: tuple[int, int], inner: tuple[int, int], modulus: int
) -> tuple[int, int]:
    """Return ``outer o inner`` for modular affine maps."""

    outer_slope, outer_offset = outer
    inner_slope, inner_offset = inner
    return (
        (outer_slope * inner_slope) % modulus,
        (outer_slope * inner_offset + outer_offset) % modulus,
    )


def edge(state: int, *, u_coordinate: bool = False) -> tuple[int, Affine, Fraction, int]:
    """Return next state, affine map, edge weight, and exponent gap."""

    if state == EVEN:
        return ODD, (T_U if u_coordinate else T_X), Fraction(1, 2), 1
    if state == ODD:
        return EVEN, (V_U if u_coordinate else V_X), Fraction(1, 32), 5
    raise ValueError("unknown parity state")


def four_family_record(
    start: int, length: int
) -> tuple[str, int, Affine, Fraction]:
    """Closed form for a nonempty path and its normalized one-way weight."""

    if length < 1:
        raise ValueError("path length must be positive")

    if start == EVEN and length % 2:
        k = (length - 1) // 2
        h = 6 * k + 1
        affine = (Fraction(2**h), Fraction(2 ** (h + 1) - 1, 3))
        coefficient = Fraction(7, 10) * Fraction(1, 2**h)
        return "EO", h, affine, coefficient

    if start == ODD and length % 2:
        k = (length - 1) // 2
        h = 6 * k + 5
        affine = (Fraction(2**h), Fraction(2**h - 2, 3))
        coefficient = Fraction(14, 5) * Fraction(1, 2**h)
        return "OE", h, affine, coefficient

    k = length // 2
    h = 6 * k
    if start == ODD:
        affine = (Fraction(2**h), Fraction(2**h - 1, 3))
        coefficient = Fraction(14, 5) * Fraction(1, 2**h)
        return "OO", h, affine, coefficient
    if start == EVEN:
        affine = (Fraction(2**h), Fraction(2 * (2**h - 1), 3))
        coefficient = Fraction(7, 10) * Fraction(1, 2**h)
        return "EE", h, affine, coefficient
    raise ValueError("unknown parity state")


def verify_path_weights(last_length: int = 16) -> None:
    """Compare deterministic T/V paths with all four closed-form families."""

    for start in (EVEN, ODD):
        state = start
        cumulative: Affine = (Fraction(1), Fraction(0))
        raw_weight = START_WEIGHT[start]
        total_gap = 0
        for length in range(1, last_length + 1):
            state, affine, transition_weight, gap = edge(state)
            cumulative = compose(affine, cumulative)
            raw_weight *= transition_weight
            total_gap += gap

            _, h, expected_affine, expected_coefficient = four_family_record(
                start, length
            )
            if total_gap != h:
                raise AssertionError("two-state path has the wrong exponent gap")
            if cumulative != expected_affine:
                raise AssertionError("two-state path has the wrong affine map")
            if NORMALIZATION * raw_weight != expected_coefficient:
                raise AssertionError("two-state path has the wrong coefficient")

    print(
        "PASS: exact T/V paths agree with all four affine coefficient families "
        f"through path length {last_length}"
    )


def verify_normalization_and_tail() -> None:
    """Check the infinite path mass and the five-cycle truncation tail."""

    even_squared_block_mass = Fraction(1, 16) / (1 - Fraction(1, 16))
    odd_squared_block_mass = Fraction(1, 4) / (1 - Fraction(1, 16))
    if even_squared_block_mass != START_WEIGHT[EVEN]:
        raise AssertionError("even squared-block starting mass changed")
    if odd_squared_block_mass != START_WEIGHT[ODD]:
        raise AssertionError("odd squared-block starting mass changed")

    even_forward = START_WEIGHT[EVEN] * (
        Fraction(1, 2) + Q
    ) / (1 - Q)
    odd_forward = START_WEIGHT[ODD] * (
        Fraction(1, 32) + Q
    ) / (1 - Q)
    if even_forward != Fraction(11, 315):
        raise AssertionError("wrong forward mass from the even state")
    if odd_forward != Fraction(4, 315):
        raise AssertionError("wrong forward mass from the odd state")
    if even_forward + odd_forward != Fraction(1, 21):
        raise AssertionError("wrong total forward unequal-pair mass")
    if 2 * NORMALIZATION * (even_forward + odd_forward) != 1:
        raise AssertionError("Green kernel is not normalized")

    # Per-orientation sums from the four closed-form rows.
    eo_mass = Fraction(7, 20) / (1 - Q)
    oe_mass = Fraction(7, 80) / (1 - Q)
    oo_mass = Fraction(14, 5) * Q / (1 - Q)
    ee_mass = Fraction(7, 10) * Q / (1 - Q)
    if (eo_mass, oe_mass, oo_mass, ee_mass) != (
        Fraction(16, 45),
        Fraction(4, 45),
        Fraction(2, 45),
        Fraction(1, 90),
    ):
        raise AssertionError("four-family infinite masses changed")
    if eo_mass + oe_mass + oo_mass + ee_mass != Fraction(1, 2):
        raise AssertionError("four-family one-way mass is not one half")

    first_omitted_cycle = 5
    tail = 2 * (
        Fraction(7, 20)
        + Fraction(7, 80)
        + Fraction(14, 5)
        + Fraction(7, 10)
    ) * Q**first_omitted_cycle / (1 - Q)
    expected_tail = 8 * Q**first_omitted_cycle
    if tail != expected_tail or tail != Fraction(1, 134_217_728):
        raise AssertionError("wrong five-cycle Green-kernel tail mass")

    print(
        "PASS: forward raw mass=1/21, symmetric normalized mass=1, "
        "and five-cycle tail=8/64^5=1/134217728"
    )


def verify_commutator() -> None:
    odd_loop = compose(T_U, V_U)
    even_loop = compose(V_U, T_U)
    if odd_loop != (Fraction(64), Fraction(49)):
        raise AssertionError("T o V changed")
    if even_loop != (Fraction(64), Fraction(56)):
        raise AssertionError("V o T changed")
    commutator = compose(even_loop, inverse(odd_loop))
    if commutator != (Fraction(1), Fraction(7)):
        raise AssertionError("loop commutator is not translation by seven")

    print(
        "PASS: (V o T) o (T o V)^-1 is the unit translation u -> u+7"
    )


def zero_matrix(size: int) -> list[list[Fraction]]:
    return [[Fraction(0) for _ in range(size)] for _ in range(size)]


def freeze(matrix: list[list[Fraction]]) -> Matrix:
    return tuple(tuple(row) for row in matrix)


def add_map_and_inverse(
    matrix: list[list[Fraction]],
    states: tuple[int, ...],
    modulus: int,
    affine: tuple[int, int],
    weight: Fraction,
) -> None:
    """Add ``weight * (U_tau + U_tau^-1)`` as permutation matrices."""

    index = {state: position for position, state in enumerate(states)}
    slope, offset = affine
    permutation = [((slope * state + offset) % modulus) for state in states]
    if set(permutation) != set(states):
        raise AssertionError("affine alignment is not a permutation of Y")
    for row, image in enumerate(permutation):
        column = index[image]
        matrix[row][column] += weight
        matrix[column][row] += weight


def path_green_kernel(level: int) -> Matrix:
    """Exact finite quotient of the two-state nonempty-path Green kernel."""

    size = 3**level
    modulus = 3 * size
    states = tuple(range(2, modulus, 3))
    matrix = zero_matrix(size)

    # After 2*size transitions, state and quotient map repeat; the path weight
    # has acquired Q^size.  Group that infinite geometric tail exactly.
    denominator = 1 - Q**size
    for start in (EVEN, ODD):
        state = start
        cumulative = (1, 0)
        raw_weight = START_WEIGHT[start]
        for _length in range(1, 2 * size + 1):
            next_state, affine_q, transition_weight, _gap = edge(state)
            affine = (int(affine_q[0]) % modulus, int(affine_q[1]) % modulus)
            cumulative = compose_mod(affine, cumulative, modulus)
            raw_weight *= transition_weight
            state = next_state
            add_map_and_inverse(
                matrix,
                states,
                modulus,
                cumulative,
                NORMALIZATION * raw_weight / denominator,
            )

        if state != start:
            raise AssertionError("two-state path did not return after a full period")
        if any((cumulative[0] * x + cumulative[1]) % modulus != x for x in states):
            raise AssertionError("path quotient did not repeat after a full period")

    result = freeze(matrix)
    if result != tuple(zip(*result)):
        raise AssertionError("path Green kernel is not symmetric")
    if any(sum(row) != 1 for row in result):
        raise AssertionError("path Green kernel is not stochastic")
    return result


def four_family_map(family: str, k: int) -> Affine:
    if family == "EO":
        h = 6 * k + 1
        return Fraction(2**h), Fraction(2 ** (h + 1) - 1, 3)
    if family == "OE":
        h = 6 * k + 5
        return Fraction(2**h), Fraction(2**h - 2, 3)
    if family == "OO":
        h = 6 * k
        return Fraction(2**h), Fraction(2**h - 1, 3)
    if family == "EE":
        h = 6 * k
        return Fraction(2**h), Fraction(2 * (2**h - 1), 3)
    raise ValueError("unknown affine family")


def four_family_coefficient(family: str, k: int) -> Fraction:
    if family == "EO":
        return Fraction(7, 20) * Q**k
    if family == "OE":
        return Fraction(7, 80) * Q**k
    if family == "OO":
        return Fraction(14, 5) * Q**k
    if family == "EE":
        return Fraction(7, 10) * Q**k
    raise ValueError("unknown affine family")


def four_family_kernel(level: int) -> Matrix:
    """Exact quotient built independently from the four affine rows."""

    size = 3**level
    modulus = 3 * size
    states = tuple(range(2, modulus, 3))
    matrix = zero_matrix(size)
    denominator = 1 - Q**size

    for family in ("EO", "OE", "OO", "EE"):
        first_k = 0 if family in ("EO", "OE") else 1
        for k in range(first_k, first_k + size):
            affine_q = four_family_map(family, k)
            affine = (int(affine_q[0]) % modulus, int(affine_q[1]) % modulus)
            weight = four_family_coefficient(family, k) / denominator
            add_map_and_inverse(matrix, states, modulus, affine, weight)

    result = freeze(matrix)
    if result != tuple(zip(*result)):
        raise AssertionError("four-family kernel is not symmetric")
    if any(sum(row) != 1 for row in result):
        raise AssertionError("four-family kernel is not stochastic")
    return result


def existing_alignment_kernel(level: int) -> Matrix:
    """Build the independently coded kernel from induced image laws."""

    modulus = 3 ** (level + 1)
    states = tuple(range(2, modulus, 3))
    image_laws = {
        state: image_distribution(state, level + 1) for state in states
    }
    raw_kernel = alignment_kernel(states, image_laws)
    return tuple(
        tuple(NORMALIZATION * entry for entry in row) for row in raw_kernel
    )


def verify_finite_kernels(last_level: int = 3) -> list[Matrix]:
    """Compare both new representations and the prior kernel exactly."""

    kernels = [path_green_kernel(0)]
    for level in range(1, last_level + 1):
        path_kernel = path_green_kernel(level)
        family_kernel = four_family_kernel(level)
        prior_kernel = existing_alignment_kernel(level)
        if path_kernel != family_kernel:
            raise AssertionError(f"path/four-family mismatch at level {level}")
        if path_kernel != prior_kernel:
            raise AssertionError(f"Green/alignment mismatch at level {level}")
        kernels.append(path_kernel)
        print(
            "PASS: exact Green kernel agrees with the existing alignment "
            f"kernel on {3**level} parent states"
        )
    return kernels


def verify_projectivity(kernels: list[Matrix]) -> None:
    """Check exact lumpability down the quotient tower."""

    for level in range(1, len(kernels)):
        fine = kernels[level]
        coarse = kernels[level - 1]
        fine_modulus = 3 ** (level + 1)
        coarse_modulus = 3**level
        fine_states = tuple(range(2, fine_modulus, 3))
        coarse_states = tuple(range(2, coarse_modulus, 3))
        coarse_index = {state: i for i, state in enumerate(coarse_states)}

        for fine_row, source in enumerate(fine_states):
            coarse_row = coarse_index[source % coarse_modulus]
            projected = [Fraction(0) for _ in coarse_states]
            for fine_column, target in enumerate(fine_states):
                coarse_column = coarse_index[target % coarse_modulus]
                projected[coarse_column] += fine[fine_row][fine_column]
            if tuple(projected) != coarse[coarse_row]:
                raise AssertionError(f"kernel is not projective at level {level}")

    print(
        "PASS: finite Green kernels are exactly projective through the "
        f"{3 ** (len(kernels) - 1)}-state quotient"
    )


def matvec(matrix: Matrix, vector: tuple[Fraction, ...]) -> tuple[Fraction, ...]:
    return tuple(
        sum(entry * value for entry, value in zip(row, vector))
        for row in matrix
    )


def inner(
    left: tuple[Fraction, ...], matrix: Matrix, right: tuple[Fraction, ...]
) -> Fraction:
    image = matvec(matrix, right)
    return sum(a * b for a, b in zip(left, image)) / len(left)


def density_vector(level: int, law: dict[int, Fraction]) -> tuple[Fraction, ...]:
    states = tuple(range(2, 3 ** (level + 1), 3))
    if set(law) != set(states):
        raise ValueError("law has the wrong quotient state space")
    return tuple(3**level * law[state] for state in states)


def detail_vector(
    level: int,
    fine_law: dict[int, Fraction],
    coarse_law: dict[int, Fraction],
) -> tuple[Fraction, ...]:
    if level < 1:
        raise ValueError("detail level must be positive")
    fine_states = tuple(range(2, 3 ** (level + 1), 3))
    coarse_modulus = 3**level
    return tuple(
        3**level * fine_law[state]
        - 3 ** (level - 1) * coarse_law[state % coarse_modulus]
        for state in fine_states
    )


def lift_detail(
    detail: tuple[Fraction, ...], source_level: int, target_level: int
) -> tuple[Fraction, ...]:
    source_states = tuple(range(2, 3 ** (source_level + 1), 3))
    source_values = dict(zip(source_states, detail))
    source_modulus = 3 ** (source_level + 1)
    target_states = tuple(range(2, 3 ** (target_level + 1), 3))
    return tuple(source_values[state % source_modulus] for state in target_states)


def verify_martingale_identities(kernels: list[Matrix]) -> None:
    """Check the projective density decomposition and collision telescoping."""

    last_kernel_level = len(kernels) - 1
    marginals: list[dict[int, Fraction]] = [{2: Fraction(1)}]
    for depth in range(1, last_kernel_level + 2):
        next_law, _image_laws = next_marginal(marginals[-1], depth)
        marginals.append(next_law)

    densities = [
        density_vector(level, marginals[level])
        for level in range(last_kernel_level + 1)
    ]
    details: list[tuple[Fraction, ...] | None] = [None]
    for level in range(1, last_kernel_level + 1):
        detail = detail_vector(level, marginals[level], marginals[level - 1])
        if sum(detail) != 0:
            raise AssertionError("martingale detail does not have Haar mean zero")
        details.append(detail)

    multipliers = [
        inner(densities[level], kernels[level], densities[level])
        for level in range(last_kernel_level + 1)
    ]
    detail_correlations: list[Fraction | None] = [None]
    for level in range(1, last_kernel_level + 1):
        detail = details[level]
        assert detail is not None
        detail_correlations.append(inner(detail, kernels[level], detail))

    # Check the orthogonal conductor decomposition at every available level.
    for level in range(last_kernel_level + 1):
        components: list[tuple[Fraction, ...]] = [
            tuple(Fraction(1) for _ in densities[level])
        ]
        for source_level in range(1, level + 1):
            detail = details[source_level]
            assert detail is not None
            components.append(lift_detail(detail, source_level, level))

        reconstructed = tuple(sum(values) for values in zip(*components))
        if reconstructed != densities[level]:
            raise AssertionError("density martingale decomposition failed")

        for i, left in enumerate(components):
            for j, right in enumerate(components):
                pairing = inner(left, kernels[level], right)
                if i != j and pairing != 0:
                    raise AssertionError("distinct conductor shells are not P-orthogonal")
                if i == j == 0 and pairing != 1:
                    raise AssertionError("constant Green-kernel pairing changed")

        diagonal_sum = Fraction(1) + sum(
            value for value in detail_correlations[1 : level + 1] if value is not None
        )
        if multipliers[level] != diagonal_sum:
            raise AssertionError("fixed-operator martingale sum failed")

    q_values = []
    for level, law in enumerate(marginals):
        q_values.append(3**level * sum(mass * mass for mass in law.values()))
    deltas = [None] + [
        q_values[level] - q_values[level - 1]
        for level in range(1, len(q_values))
    ]

    for level in range(last_kernel_level + 1):
        delta = deltas[level + 1]
        assert delta is not None
        if delta != Fraction(2, 7) * multipliers[level]:
            raise AssertionError("collision renewal multiplier identity failed")

    for level in range(1, last_kernel_level + 1):
        correlation = detail_correlations[level]
        delta_here = deltas[level]
        delta_next = deltas[level + 1]
        assert correlation is not None
        assert delta_here is not None and delta_next is not None
        if correlation != multipliers[level] - multipliers[level - 1]:
            raise AssertionError("martingale Green pairing did not telescope")
        if correlation != Fraction(7, 2) * (delta_next - delta_here):
            raise AssertionError("detail-energy telescoping identity failed")

    expected_first_detail = Fraction(-2086, 67963)
    if detail_correlations[1] != expected_first_detail:
        raise AssertionError("exact first detail-shell correlation changed")

    print(
        "PASS: fixed-projective P martingale decomposition and collision-energy "
        f"telescoping hold through detail level {last_kernel_level}"
    )
    print(
        "PASS: first detail-shell pairing is exactly "
        "<d_1,P d_1>=-2086/67963"
    )


def main() -> None:
    verify_path_weights()
    verify_normalization_and_tail()
    verify_commutator()
    kernels = verify_finite_kernels()
    verify_projectivity(kernels)
    verify_martingale_identities(kernels)
    print(
        "CONCLUSION: the bounded exact Green-kernel, quotient, and martingale "
        "identities pass; no affine local-limit asymptotic is certified"
    )


if __name__ == "__main__":
    main()
