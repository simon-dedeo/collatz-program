#!/usr/bin/env python3
"""Sharded exact search over non-uniform binary morphic valuation programs.

The fixed point of a binary morphism ``phi`` prolongable on 0 is generated by
``phi^d(0)``.  Unlike the uniform search in search_programs.py, image lengths
may differ, so this includes growing gaps and simple one-counter-like word
geometry.  Each symbol is coded by a valuation in ``1..max_k``.

The search uses compressed affine blocks carrying ``(3^N, 2^S, A)``.  It
tests exact positive cycle division at every bounded depth and, at a separate
word-length bound, synthesizes the canonical seeds in both classes modulo 6
and checks stabilization across nested prefixes.  Any reported event is
expanded and literally replayed with path_compiler.py.

Sharding is deterministic by the lexicographic morphism index.  Every shard
reports its exact range and source digest; merge_nonuniform.py refuses missing
or inconsistent shards.
"""

from __future__ import annotations

import argparse
import hashlib
import itertools
import json
from dataclasses import dataclass
from pathlib import Path

from path_compiler import (
    accelerated_step,
    cycle_certificate,
    replay_word,
)


@dataclass(frozen=True)
class FastBlock:
    steps: int
    halvings: int
    three_power: int
    two_power: int
    constant: int

    @staticmethod
    def identity() -> "FastBlock":
        return FastBlock(0, 0, 1, 1, 0)

    @staticmethod
    def letter(k: int) -> "FastBlock":
        if k <= 0:
            raise ValueError("valuation coding must be positive")
        return FastBlock(1, k, 3, 1 << k, 1)

    def then(self, other: "FastBlock") -> "FastBlock":
        return FastBlock(
            self.steps + other.steps,
            self.halvings + other.halvings,
            self.three_power * other.three_power,
            self.two_power * other.two_power,
            other.three_power * self.constant + self.two_power * other.constant,
        )

    @property
    def cycle_denominator(self) -> int:
        return self.two_power - self.three_power


def binary_words(max_length: int, first: int | None = None):
    for length in range(1, max_length + 1):
        if first is None:
            yield from itertools.product((0, 1), repeat=length)
        else:
            for tail in itertools.product((0, 1), repeat=length - 1):
                yield (first,) + tail


def morphism_count(max_image_length: int) -> int:
    # Nonempty image of 0 beginning in 0, and an arbitrary nonempty image of 1.
    return ((1 << max_image_length) - 1) * ((1 << (max_image_length + 1)) - 2)


def morphisms(max_image_length: int):
    image_zeros = list(binary_words(max_image_length, first=0))
    image_ones = list(binary_words(max_image_length))
    index = 0
    for image_zero in image_zeros:
        for image_one in image_ones:
            yield index, (image_zero, image_one)
            index += 1


def substitute_blocks(
    blocks: tuple[FastBlock, FastBlock],
    image: tuple[tuple[int, ...], tuple[int, ...]],
) -> tuple[FastBlock, FastBlock]:
    out = []
    for symbol in (0, 1):
        block = FastBlock.identity()
        for child in image[symbol]:
            block = block.then(blocks[child])
        out.append(block)
    return out[0], out[1]


def expand(
    image: tuple[tuple[int, ...], tuple[int, ...]], depth: int
) -> tuple[int, ...]:
    word = (0,)
    for _ in range(depth):
        word = tuple(child for symbol in word for child in image[symbol])
    return word


def canonical_seed(block: FastBlock, residue_mod_6: int) -> tuple[int, int]:
    if residue_mod_6 not in (1, 5):
        raise ValueError("residue must be 1 or 5 modulo 6")
    modulus_two = 2 * block.two_power
    residue_two = (
        (block.two_power - block.constant)
        * pow(block.three_power, -1, modulus_two)
    ) % modulus_two
    lift = (
        (residue_mod_6 - residue_two) * pow(modulus_two, -1, 3)
    ) % 3
    seed = residue_two + modulus_two * lift
    endpoint_numerator = block.three_power * seed + block.constant
    if endpoint_numerator % block.two_power:
        raise AssertionError("compiled endpoint is not integral")
    return seed, endpoint_numerator // block.two_power


def trace_event(
    seed: int,
    image: tuple[tuple[int, ...], tuple[int, ...]],
    coding: tuple[int, int],
    depth: int,
    expected_endpoint: int,
) -> tuple[int | None, tuple[int, ...]]:
    symbols = expand(image, depth)
    word = tuple(coding[symbol] for symbol in symbols)
    if replay_word(seed, word) != expected_endpoint:
        raise AssertionError("compressed seed event failed literal replay")
    n = seed
    first_one_step = 0 if n == 1 else None
    for step, k in enumerate(word, start=1):
        n, actual_k = accelerated_step(n)
        if actual_k != k:
            raise AssertionError("literal trace valuation mismatch")
        if n == 1 and first_one_step is None:
            first_one_step = step
    return first_one_step, word


def bounded_fate(seed: int, max_steps: int) -> dict[str, object]:
    n = seed
    peak = n
    seen: dict[int, int] = {}
    for step in range(max_steps + 1):
        if n == 1:
            return {
                "status": "reaches_one",
                "accelerated_steps": step,
                "peak": str(peak),
            }
        if n in seen:
            return {
                "status": "cycle",
                "preperiod": seen[n],
                "period": step - seen[n],
                "cycle_seed": str(n),
                "peak": str(peak),
            }
        if step == max_steps:
            break
        seen[n] = step
        n, _ = accelerated_step(n)
        peak = max(peak, n)
    return {
        "status": "unresolved_within_bound",
        "max_accelerated_steps": max_steps,
        "last_value": str(n),
        "peak": str(peak),
    }


def event_key(event: dict[str, object] | None) -> tuple[int, int, int]:
    if event is None:
        return (-1, -1, -1)
    return (
        int(event["stable_extensions"]),
        int(event["word_length"]),
        -int(event["seed"]),
    )


def search_shard(
    *,
    max_image_length: int,
    max_k: int,
    max_depth: int,
    max_word_length: int,
    max_seed_word_length: int,
    continuation_steps: int,
    shard_index: int,
    shards: int,
) -> dict[str, object]:
    if not (
        max_image_length >= 1
        and max_k >= 1
        and max_depth >= 1
        and 1 <= max_seed_word_length <= max_word_length
        and continuation_steps >= 1
        and shards >= 1
        and 0 <= shard_index < shards
    ):
        raise ValueError("invalid search bounds or shard")

    morphisms_seen = 0
    morphisms_searched = 0
    coding_templates = 0
    depth_instances = 0
    positive_multiplier_instances = 0
    exact_cycle_instances = 0
    trivial_cycle_instances = 0
    nontrivial_cycles: list[dict[str, object]] = []
    stabilization_events = 0
    nontrivial_stabilization_events = 0
    one_avoiding_stabilization_events = 0
    longest_stabilization = None
    longest_nontrivial_stabilization = None
    longest_one_avoiding_stabilization = None

    for morphism_index, image in morphisms(max_image_length):
        morphisms_seen += 1
        if morphism_index % shards != shard_index:
            continue
        morphisms_searched += 1
        for coding in itertools.product(range(1, max_k + 1), repeat=2):
            coding_templates += 1
            blocks = (FastBlock.letter(coding[0]), FastBlock.letter(coding[1]))
            previous_seed: dict[int, int | None] = {1: None, 5: None}
            stable_extensions = {1: 0, 5: 0}
            previous_steps = 1
            for depth in range(1, max_depth + 1):
                blocks = substitute_blocks(blocks, image)
                block = blocks[0]
                if block.steps > max_word_length:
                    break
                depth_instances += 1
                if block.cycle_denominator > 0:
                    positive_multiplier_instances += 1
                    denominator = block.cycle_denominator
                    if block.constant % denominator == 0:
                        exact_cycle_instances += 1
                        symbols = expand(image, depth)
                        word = tuple(coding[s] for s in symbols)
                        cert = cycle_certificate(word)
                        if cert is None:
                            raise AssertionError("compressed cycle hit failed replay")
                        if cert.seed == 1:
                            trivial_cycle_instances += 1
                        else:
                            nontrivial_cycles.append(cert.json_dict())

                if block.steps <= max_seed_word_length:
                    for residue in (1, 5):
                        seed, endpoint = canonical_seed(block, residue)
                        if seed == previous_seed[residue]:
                            stable_extensions[residue] += 1
                            stabilization_events += 1
                            first_one, _ = trace_event(
                                seed, image, coding, depth, endpoint
                            )
                            event = {
                                "morphism_index": morphism_index,
                                "image_0": "".join(map(str, image[0])),
                                "image_1": "".join(map(str, image[1])),
                                "coding": list(coding),
                                "residue_mod_6": residue,
                                "depth": depth,
                                "word_length": block.steps,
                                "stable_extensions": stable_extensions[residue],
                                "seed": str(seed),
                                "first_one_step": first_one,
                            }
                            if event_key(event) > event_key(longest_stabilization):
                                longest_stabilization = event
                            if seed != 1:
                                nontrivial_stabilization_events += 1
                                if event_key(event) > event_key(
                                    longest_nontrivial_stabilization
                                ):
                                    longest_nontrivial_stabilization = event
                            if first_one is None:
                                one_avoiding_stabilization_events += 1
                                if event_key(event) > event_key(
                                    longest_one_avoiding_stabilization
                                ):
                                    longest_one_avoiding_stabilization = event
                        else:
                            stable_extensions[residue] = 0
                        previous_seed[residue] = seed

                # If phi^d(0) stopped growing, later depths duplicate the same
                # program and add no new bounded information.
                if block.steps == previous_steps:
                    break
                previous_steps = block.steps

    if longest_one_avoiding_stabilization is not None:
        longest_one_avoiding_stabilization = dict(longest_one_avoiding_stabilization)
        longest_one_avoiding_stabilization["continuation"] = bounded_fate(
            int(longest_one_avoiding_stabilization["seed"]), continuation_steps
        )

    source = Path(__file__).read_bytes() + Path(__file__).with_name(
        "path_compiler.py"
    ).read_bytes()
    return {
        "schema": "collatz-nonuniform-morphic-search-shard-v1",
        "arithmetic": "exact_python_integers",
        "source_sha256": hashlib.sha256(source).hexdigest(),
        "bounds": {
            "max_image_length": max_image_length,
            "max_k": max_k,
            "max_depth": max_depth,
            "max_word_length": max_word_length,
            "max_seed_word_length": max_seed_word_length,
            "continuation_steps": continuation_steps,
        },
        "shard_index": shard_index,
        "shards": shards,
        "total_morphisms_in_class": morphism_count(max_image_length),
        "morphisms_seen": morphisms_seen,
        "morphisms_searched": morphisms_searched,
        "coding_templates": coding_templates,
        "depth_instances": depth_instances,
        "positive_multiplier_instances": positive_multiplier_instances,
        "exact_cycle_instances": exact_cycle_instances,
        "trivial_cycle_instances": trivial_cycle_instances,
        "nontrivial_cycle_instances": len(nontrivial_cycles),
        "nontrivial_cycles": nontrivial_cycles,
        "seed_stabilization_events": stabilization_events,
        "nontrivial_seed_stabilization_events": nontrivial_stabilization_events,
        "one_avoiding_seed_stabilization_events": one_avoiding_stabilization_events,
        "longest_seed_stabilization": longest_stabilization,
        "longest_nontrivial_seed_stabilization": longest_nontrivial_stabilization,
        "longest_one_avoiding_seed_stabilization": longest_one_avoiding_stabilization,
    }


def selftest() -> None:
    from path_compiler import affine_block

    assert morphism_count(1) == 2
    assert morphism_count(3) == 98
    image = ((0, 1), (1, 0, 1))
    coding = (1, 3)
    blocks = (FastBlock.letter(1), FastBlock.letter(3))
    for depth in range(1, 5):
        blocks = substitute_blocks(blocks, image)
        symbols = expand(image, depth)
        word = tuple(coding[s] for s in symbols)
        reference = affine_block(word)
        assert blocks[0].steps == reference.steps
        assert blocks[0].halvings == reference.halvings
        assert blocks[0].three_power == reference.three_power
        assert blocks[0].two_power == reference.two_power
        assert blocks[0].constant == reference.constant
        for residue in (1, 5):
            seed, endpoint = canonical_seed(blocks[0], residue)
            assert replay_word(seed, word) == endpoint

    common = dict(
        max_image_length=2,
        max_k=2,
        max_depth=4,
        max_word_length=64,
        max_seed_word_length=32,
        continuation_steps=1000,
    )
    whole = search_shard(**common, shard_index=0, shards=1)
    halves = [search_shard(**common, shard_index=i, shards=2) for i in range(2)]
    for key in (
        "morphisms_searched",
        "coding_templates",
        "depth_instances",
        "positive_multiplier_instances",
        "exact_cycle_instances",
        "trivial_cycle_instances",
        "nontrivial_cycle_instances",
        "seed_stabilization_events",
        "nontrivial_seed_stabilization_events",
        "one_avoiding_seed_stabilization_events",
    ):
        assert whole[key] == sum(part[key] for part in halves), key


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--max-image-length", type=int, default=6)
    parser.add_argument("--max-k", type=int, default=4)
    parser.add_argument("--max-depth", type=int, default=12)
    parser.add_argument("--max-word-length", type=int, default=4096)
    parser.add_argument("--max-seed-word-length", type=int, default=512)
    parser.add_argument("--continuation-steps", type=int, default=10000)
    parser.add_argument("--shard-index", type=int, default=0)
    parser.add_argument("--shards", type=int, default=1)
    parser.add_argument("--output", type=Path)
    parser.add_argument("--selftest", action="store_true")
    args = parser.parse_args()
    if args.selftest:
        selftest()
        print("search_nonuniform selftest: PASS")
        return
    result = search_shard(
        max_image_length=args.max_image_length,
        max_k=args.max_k,
        max_depth=args.max_depth,
        max_word_length=args.max_word_length,
        max_seed_word_length=args.max_seed_word_length,
        continuation_steps=args.continuation_steps,
        shard_index=args.shard_index,
        shards=args.shards,
    )
    rendered = json.dumps(result, indent=2, sort_keys=True) + "\n"
    if args.output:
        args.output.write_text(rendered)
        print(f"wrote {args.output}")
    else:
        print(rendered, end="")


if __name__ == "__main__":
    main()
